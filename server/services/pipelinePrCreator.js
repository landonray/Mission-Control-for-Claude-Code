'use strict';

const { execFile } = require('child_process');
const util = require('util');
const execFileAsync = util.promisify(execFile);

// Test seam — replaced in unit tests so we don't shell out.
let _runGit = defaultRunGit;
let _runGh = defaultRunGh;

function _setExecutorsForTests({ runGit, runGh } = {}) {
  if (runGit) _runGit = runGit;
  if (runGh) _runGh = runGh;
}

function _resetExecutorsForTests() {
  _runGit = defaultRunGit;
  _runGh = defaultRunGh;
}

async function defaultRunGit(args, { cwd }) {
  return execFileAsync('git', args, { cwd, maxBuffer: 10 * 1024 * 1024 });
}

async function defaultRunGh(args, { cwd }) {
  return execFileAsync('gh', args, { cwd, maxBuffer: 10 * 1024 * 1024 });
}

function buildPrTitle(pipelineName) {
  const trimmed = String(pipelineName || '').trim();
  return trimmed ? `pipeline: ${trimmed}` : 'pipeline: build';
}

function buildPrBody({ pipelineName, specInput, pipelineId }) {
  const spec = String(specInput || '').trim();
  const lines = [
    `Built by Mission Control pipeline \`${pipelineId}\` — _${pipelineName}_`,
    '',
    '## Spec',
    '',
    spec || '_(no spec input recorded)_',
    '',
    '---',
    '_Generated by Mission Control. Review the diff before merging._',
  ];
  return lines.join('\n');
}

async function pushBranch({ projectRootPath, branchName }) {
  await _runGit(['push', '-u', 'origin', branchName], { cwd: projectRootPath });
}

async function findExistingPrUrl({ projectRootPath, branchName }) {
  try {
    const { stdout } = await _runGh(
      ['pr', 'view', branchName, '--json', 'url', '-q', '.url'],
      { cwd: projectRootPath }
    );
    const url = String(stdout || '').trim();
    return url || null;
  } catch (_) {
    return null;
  }
}

/**
 * Creates a PR for the given pipeline branch. If a PR already exists, returns
 * its URL instead of failing. Pushes the branch first so the remote has it.
 *
 * Returns: { url, existed } on success.
 * Throws on unrecoverable failure (auth missing, no remote, etc.) — caller
 * should catch and surface the error message to the user.
 */
async function createPullRequest({
  projectRootPath,
  branchName,
  pipelineName,
  pipelineId,
  specInput,
  baseBranch = 'main',
}) {
  if (!projectRootPath) throw new Error('projectRootPath required');
  if (!branchName) throw new Error('branchName required');

  await pushBranch({ projectRootPath, branchName });

  const title = buildPrTitle(pipelineName);
  const body = buildPrBody({ pipelineName, specInput, pipelineId });

  try {
    const { stdout } = await _runGh(
      ['pr', 'create', '--base', baseBranch, '--head', branchName, '--title', title, '--body', body],
      { cwd: projectRootPath }
    );
    const match = String(stdout || '').match(/https?:\/\/\S+/);
    if (match) return { url: match[0], existed: false };
    const fallback = await findExistingPrUrl({ projectRootPath, branchName });
    if (fallback) return { url: fallback, existed: true };
    throw new Error('gh pr create succeeded but did not return a URL');
  } catch (err) {
    const stderr = String(err.stderr || err.message || '');
    if (/already exists/i.test(stderr) || /a pull request for branch/i.test(stderr)) {
      const existing = await findExistingPrUrl({ projectRootPath, branchName });
      if (existing) return { url: existing, existed: true };
    }
    throw new Error(`gh pr create failed: ${stderr.split('\n')[0] || err.message}`);
  }
}

module.exports = {
  createPullRequest,
  buildPrTitle,
  buildPrBody,
  _setExecutorsForTests,
  _resetExecutorsForTests,
};
